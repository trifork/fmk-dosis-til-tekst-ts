export class MMANMapping {

    morning: number;
    noon: number;
    evening: number;
    night: number;

    public getMorning() {
        return this.morning;
    }

    public setMorning(morning: number) {
        this.morning = morning;
    }

    public getNoon() {
        return this.noon;
    }

    public setNoon(noon: number) {
        this.noon = noon;
    }

    public getEvening() {
        return this.evening;
    }
    public setEvening(evening: number) {
        this.evening = evening;
    }

    public getNight() {
        return this.night;
    }

    public setNight(night: number) {
        this.night = night;
    }
}